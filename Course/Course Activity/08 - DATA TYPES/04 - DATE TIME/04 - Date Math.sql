select name, birthdate, datediff(now(), birthdate) as DateDiff from people; -- always results in days
/*
+---------+------------+----------+
| name    | birthdate  | DateDiff |
+---------+------------+----------+
| Padma   | 1983-11-11 |    12711 |
| Larry   | 1943-12-25 |    27277 |
| Toaster | 2018-08-29 |        1 |
+---------+------------+----------+
*/

select name, birthdate, date_add(birthdate, INTERVAL 3 QUARTER) as DateAdd from people;

/*
+---------+------------+------------+
| name    | birthdate  | DateAdd    |
+---------+------------+------------+
| Padma   | 1983-11-11 | 1984-08-11 |
| Larry   | 1943-12-25 | 1944-09-25 |
| Toaster | 2018-08-29 | 2019-05-29 |
+---------+------------+------------+
*/

select name, birthdt, birthdt+INTERVAL 3 QUARTER as DateAdd_Shortcut from people;

/*
+---------+---------------------+---------------------+
| name    | birthdt             | DateAdd_Shortcut    |
+---------+---------------------+---------------------+
| Padma   | 1983-11-11 10:07:35 | 1984-08-11 10:07:35 |
| Larry   | 1943-12-25 04:10:42 | 1944-09-25 04:10:42 |
| Toaster | 2018-08-29 11:55:00 | 2019-05-29 11:55:00 |
+---------+---------------------+---------------------+
*/

select name, birthdt, birthdt+ INTERVAL 15 MONTH + INTERVAL 10 HOUR from people;

/*
+---------+---------------------+-----------------------------------------------+
| name    | birthdt             | birthdt+ INTERVAL 15 MONTH + INTERVAL 10 HOUR |
+---------+---------------------+-----------------------------------------------+
| Padma   | 1983-11-11 10:07:35 | 1985-02-11 20:07:35                           |
| Larry   | 1943-12-25 04:10:42 | 1945-03-25 14:10:42                           |
| Toaster | 2018-08-29 11:55:00 | 2019-11-29 21:55:00                           |
+---------+---------------------+-----------------------------------------------+
*/