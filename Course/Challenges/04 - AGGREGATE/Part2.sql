/* Q5 Find the full name of the author who wrote the longest book */

select concat(author_fname, ' ' , author_lname) as 'Result' from books
where pages = (select max(pages) from books);

/*
+----------------+
| Result         |
+----------------+
| Michael Chabon |
+----------------+
*/

